package com.pertemuan_3;

public class OpPerbandingan {
    

    public static void main(String[] args) {

        // Operasi Perbandingan
        int value1 = 100;
        int value2 = 100;

        System.out.println(value1 > value2);
        System.out.println(value1 < value2);
        System.out.println(value1 >= value2);
        System.out.println(value1 <= value2);
        System.out.println(value1 == value2);
        System.out.println(value1 != value2);

    }

}
