package com.pertemuan_3;

public class OpPerbandingan {
    
    public static void main(String[] args) {

        // Operasi Perbandingan
        int value1 = 100;
        int value2 = 90;

        // Memeriksa apakah value1 lebih besar dari value2
        System.out.println(value1 > value2); // Output: false

        // Memeriksa apakah value1 lebih kecil dari value2
        System.out.println(value1 < value2); // Output: false

        // Memeriksa apakah value1 lebih besar atau sama dengan value2
        System.out.println(value1 >= value2); // Output: true

        // Memeriksa apakah value1 lebih kecil atau sama dengan value2
        System.out.println(value1 <= value2); // Output: true

        // Memeriksa apakah value1 sama dengan value2
        System.out.println(value1 == value2); // Output: true

        // Memeriksa apakah value1 tidak sama dengan value2
        System.out.println(value1 != value2); // Output: false

    }

}
