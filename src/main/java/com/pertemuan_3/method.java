package com.pertemuan_3;

public class method {
    public static void main(String[] args) {
        // Memanggil metode sayHelloWorld() saat program dijalankan.
        sayHelloWorld();
    }

    // Deklarasi metode sayHelloWorld() yang digunakan dalam metode main.
    static void sayHelloWorld() {
        // Menampilkan pesan "Good Morning Joeee" ke konsol.
        System.out.println("Good Morning Joeee");
    }
}
